#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn viridis_endpoints_and_midpoint() {
        // Index 0: dark purple
        let c0 = viridis(0.0);
        assert_eq!(c0.r, 68);
        assert_eq!(c0.g, 1);
        assert_eq!(c0.b, 84);

        // Index 255: yellow
        let c1 = viridis(1.0);
        assert_eq!(c1.r, 253);
        assert_eq!(c1.g, 231);
        assert_eq!(c1.b, 37);

        // Index ~128: teal-ish (green channel dominant, high blue, low red)
        let cm = viridis(0.5);
        assert!(cm.g > cm.r, "midpoint green should exceed red");
        assert!(cm.b > cm.r, "midpoint blue should exceed red");
        assert!(cm.g >= 130, "midpoint green should be teal-range (>= 130)");
    }

    #[test]
    fn viridis_clamps_out_of_range() {
        let lo = viridis(-1.0);
        assert_eq!(lo, viridis(0.0));
        let hi = viridis(2.0);
        assert_eq!(hi, viridis(1.0));
    }
}
