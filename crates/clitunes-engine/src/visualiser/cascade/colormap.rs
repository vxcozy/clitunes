//! Viridis 256-colour perceptually-uniform colormap.
//!
//! Derived from the matplotlib viridis palette (dark purple -> teal ->
//! yellow). The 256-entry LUT is linearly interpolated in RGB space from
//! 17 reference control points.

use crate::visualiser::cell_grid::Rgb;

/// Map a value in `[0.0, 1.0]` to a viridis colour.
pub fn viridis(t: f32) -> Rgb {
    let t = t.clamp(0.0, 1.0);
    let idx = (t * 255.0) as usize;
    let (r, g, b) = VIRIDIS_LUT[idx.min(255)];
    Rgb::new(r, g, b)
}

